 <p align="justify">Internationale Abende werden regelmäßig im IntClub veranstaltet. Allerdings sind nur ausländische Gemeinden mit vielen Studierenden in der Lage, solche Veranstaltungen zu organisieren. Außerdem konnten wir bis jetzt an einem Abend nur eine Kultur erleben und nur die Gerichte aus dieser Kultur probieren. Einige waren gut, andere besser. Und jetzt ist es Zeit, diese zu vergleichen.
     <br/><br/>
International Nite: Taste me before you judge me (INT) soll die Präsentation von Ländern mit nur wenigen Repräsentanten in Kaiserslautern und deren Kulturen ermöglichen. Es gibt einige Kategorien, die Sie als Teilnehmer wählen können, um sich nicht unter Druck zu setzen. Außerdem können Sie ein traditionelles Lied ( Link auf YouTube ) angeben, das  während des Abends gespielt wird. Gerne können größere Teams in mehreren Kategorien konkurrieren. Es können auch mehrere Teams aus einem Land an dem Wettbewerb teilnehmen. 
<br/><br/>
Der Wettbewerb unter Studenten aus verschiedenen Ländern in verschiedenen Kategorien von Gerichten soll zeigen, worauf jede Kultur etwas stolz sein kann. In der Welt voller negativer Vorurteile wollen wir Länder bestimmten Geschmacksrichtungen zuordnen. Nächstes Mal, wenn Sie über ein weit entferntes Land  hören, lassen Sie das erste, was Ihnen dazu einfällt, süß, scharf oder das Erstaunlichste, was ich je probiert habe, sein. 
<br/><br/>
Sprechen Sie mit Ihren Freunden, registrieren Sie sich auf unserer Website, bereiten Sie ein Gericht vor und behmen Sie am Wettbewerb teil! Falls Sie nur ein Besucher sind, seien Sie bereit, eine große Anzahl an Gerichten auszuprobieren und ein ehrliches Feedback abzugeben. Falls Sie weitere Fragen haben, schreiben Sie uns einfach an  eestec@rhrk.uni-kl.de .
